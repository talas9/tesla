async def exec_command(args: list, timeout=None, user: str='odin_script', detached: bool=False, current_working_dir: str=None) -> Dict[(str, Union[(int, str)])]:
    if current_working_dir == "":
        current_working_dir = None
    validate_path_absolute(current_working_dir)
    proc = await create_subprocess_exec(*args, stdout=asyncio.subprocess.PIPE, 
     stderr=asyncio.subprocess.PIPE, 
     preexec_fn=security.demote(user), 
     env=get_subprocess_env(), 
     cwd=current_working_dir)
    task = proc.communicate()
    if detached:
        asyncio.ensure_future(task)
        return {
         'exit_signal': 0, 
         'exit_status': 0, 
         'stdout': '""', 
         'stderr': '""'}
    else:
        if timeout:
            stdout, stderr = await asyncio.wait_for(task, timeout)
        else:
            stdout, stderr = await task
        returncode = await proc.wait()
        if returncode:
            exit_signal = abs(returncode) if returncode < 0 else None
            if sys.stdout.encoding == "ascii":
                encoding = "utf-8"
        else:
            encoding = sys.stdout.encoding
        return {'exit_signal':exit_signal, 
         'exit_status':returncode, 
         'stdout':stdout.decode(encoding, "backslashreplace"), 
         'stderr':stderr.decode(encoding, "backslashreplace")}


