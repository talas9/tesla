async def active_alerts(timeout: int=settings.DEFAULT_TIMEOUT, audience: Optional[Set[str]]=None) -> AsyncGenerator[(Tuple[(str, Dict[(str, Any)])], None)]:
    session = await odin.get_http_session()
    url = get_url("carserver") + "alerts"
    all_audiences = {'customer', 'service', 'service-fix', 'factory', 'no audience', None}
    audience = audience or all_audiences
    try:
        async with session.get(url, timeout=timeout) as resp:
            resp.raise_for_status()
            raw_alerts = await resp.json()
    except asyncio.TimeoutError:
        raise RuntimeError("Failed to retrieve alerts: timed out.")
    else:
        for raw_alert, signal_audience, debug_text, value in raw_alerts:
            try:
                alert_name, user_message = ALERT_PATTERN.match(raw_alert).groups()
            except (re.error, ValueError):
                log.exception("Invalid alert pattern: %s", raw_alert)
            else:
                signal_audience = set([x.strip() for x in signal_audience.strip(whitespace + "[]").split(",")])
                if not audience.isdisjoint(signal_audience):
                    yield (
                     alert_name,
                     {
                      'user_message': user_message, 
                      'audience': signal_audience, 
                      'debug_text': debug_text, 
                      'value': value})
                else:
                    if len(signal_audience - all_audiences) > 0:
                        log.error("Invalid alert audiences: %s", list(signal_audience - all_audiences))


